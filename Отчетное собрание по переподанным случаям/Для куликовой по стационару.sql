SELECT s.USL_OK, 
(SELECT f.NameWithID FROM F002 AS f WHERE f.smocod = p.SMO)smo,  
 sum(s.SUMV)SUMV, COUNT(*)kol
FROM SLUCH AS s
JOIN PACIENT AS p ON p.ID = s.PID
JOIN D3_SCHET_OMS AS dso ON dso.ID = s.SCHET_ID
JOIN USL AS u ON u.SLID = s.ID
WHERE s.SCHET_ID IN (2057,2059,2060,2061,2071,2077,2083,2092,2095,2111,2115, 2119)
AND dso.[MONTH] = MONTH(s.DATE_2)
and --CODE_USL = 'TF1.D01.001'
--AND u.LPU_1 = 201 
--AND u.PODR IN (9)
CODE_USL = 'TF1.K01.001'
AND u.LPU_1 = 101 AND u.PODR = 1
GROUP BY smo, s.USL_OK