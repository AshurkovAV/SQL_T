
SELECT
(SELECT pd.NameWithID FROM OtdelDb AS pd WHERE pd.Id = sl.podr) podr,
 SUM(sumv), sc.[MONTH]
 
FROM D3_ZSL_OMS zsl 
JOIN D3_SL_OMS sl ON zsl.ID = sl.D3_ZSLID
JOIN D3_SCHET_OMS sc ON zsl.D3_SCID = sc.ID 
JOIN D3_PACIENT_OMS pa ON zsl.D3_PID = pa.ID 
WHERE sc.[YEAR] = 2019 AND sc.CODE_MO = 460026 
--zsl.D3_SCID IN (2117,2129,2136,2137,2141,2142,2146,2147,2152,2153,2155,2156,2159,2160,2162,2168,2170,2174,2176,2180,2186,2187,2189,2190) 
--AND (zsl.PR_NOV = 0 OR zsl.PR_NOV IS NULL) 
AND zsl.USL_OK IN (1,2)               
GROUP BY sl.PODR, sc.[MONTH]
ORDER BY podr, sc.[MONTH]