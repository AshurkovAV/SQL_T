SELECT 
s.[MONTH] ,
SUM(CASE WHEN CODE_USL = 'TF1.D01.001' AND u.LPU_1 = 201 AND u.PODR = 9 THEN sumv ELSE 0  END) 'Дневной стационар',
SUM(CASE WHEN CODE_USL = 'TF1.K01.001' AND u.LPU_1 = 101 AND u.PODR = 8 THEN sumv ELSE 0  END) 'КC пульмонология',
SUM(CASE WHEN CODE_USL = 'TF1.K01.001' AND u.LPU_1 = 101 AND u.PODR = 1 THEN sumv ELSE 0  END) 'КC терапия'
  FROM [SLUCH] sl
  LEFT JOIN USL AS u ON u.SLID = sl.ID
  INNER JOIN PACIENT AS p ON p.ID = sl.PID
  LEFT JOIN (SELECT SLID, dbo.GROUP_CONCAT_DS(DISTINCT DS, N' , ', 1)DS FROM SLUCH_DS2 GROUP BY SLID)AS sd2  ON sd2.SLID = sl.ID
  JOIN SCHET AS s ON s.ID = sl.SCHET_ID
WHERE 
sl.SCHET_ID IN (1035,1037,1039,1042,1044,1046,1047,1048,1050,2051,2053,2055)
AND sl.USL_OK IN (1,2) AND
MONTH(sl.DATE_2) = s.[MONTH] 
GROUP BY s.[MONTH] 
