SELECT p.ID_ENP, COUNT(*)
FROM AttachedPeople_ONLINE AS p
LEFT JOIN AttachedCustomAdr_ONLINE AS adr ON adr.PID = p.ID
LEFT JOIN AttachedLpu_ONLINE AS lpu ON lpu.PID = p.ID AND lpu.D_Otk IS NULL
LEFT JOIN AttachedUchastok_ONLINE AS uch ON uch.ID = lpu.UCH_ID
WHERE 
p.ID_SMO = 46004
AND p.x_Status = 1
GROUP BY p.ID_ENP
HAVING COUNT(*) > 1

