SELECT ysme.NameWithID, COUNT(*)
FROM D3_ZSL_OMS AS dzo
LEFT JOIN D3_SL_OMS AS dso ON dso.D3_ZSLID = dzo.ID
LEFT JOIN Yamed_Spr_MedicalEmployee AS ysme ON ysme.SNILS = dso.IDDOKT
WHERE dso.IDDOKT IN (
'048-124-474 49',
'054-827-304 66',
'112-653-849 44',
'073-493-129 73',
'150-859-169 81',
'151-461-173 33',
'147-202-024 37'	
) AND dzo.D3_SCID IN (2057,2059,2060,2061,2071,2077,2083,2092,2095,2111,2115,2119)
GROUP BY ysme.NameWithID