SELECT duo.COMENTU, MONTH(dzo.DATE_Z_2)--, duo.VID_VME,  
,COUNT(*)
FROM D3_ZSL_OMS AS dzo
JOIN D3_SL_OMS AS dso ON dso.D3_ZSLID = dzo.ID
JOIN D3_USL_OMS AS duo ON duo.D3_SLID = dso.ID
JOIN D3_SCHET_OMS AS dso2 ON dso2.ID = dzo.D3_SCID
WHERE dso2.ID IN (4413, 4416, 4399)-- dso2.NSCHET LIKE '%Основно%Сдан%' 
AND dzo.USL_OK = 3 AND dso.PROFIL NOT IN (63,85,86,87,88,89,90)
--AND dzo.DATE_Z_2 >= '20210101' 
GROUP BY duo.COMENTU, MONTH(dzo.DATE_Z_2) --, duo.VID_VME