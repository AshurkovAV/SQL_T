SELECT DISTINCT ZSL.D3_SCID,dso.ds1
,zsl.ZSL_ID , duo.id,duo.CODE_USL, y.NameWithID
FROM
D3_ZSL_OMS zsl  
JOIN D3_SL_OMS AS dso ON dso.D3_ZSLID = zsl.ID
JOIN D3_USL_OMS AS duo ON duo.D3_SLID = dso.ID
left join Yamed_Spr_UslCode y on y.ID=duo.vid_vme	
WHERE zsl_id IN (
SELECT dzo.ZSL_ID --, MAX(DUO.ID)
 FROM D3_ZSL_OMS AS dzo
 JOIN D3_SCHET_OMS AS sch ON sch.ID=dzo.D3_SCID
JOIN D3_SL_OMS AS dso ON dso.D3_ZSLID = dzo.ID
LEFT JOIN D3_USL_OMS AS duo ON duo.D3_SLID = dso.ID
WHERE
 --dzo.D3_SCID = 4038--но€брь
